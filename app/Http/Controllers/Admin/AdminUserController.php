    protected function createAdmin(Request $request)
    {
         $user = $this->user::create([
            'name' => $request->name,
            'email' => $request->email,
            'password' => Hash::make($request->password),
            'role' => 'admin',
        ]);
        Auth::loginUsingId($user->id);
        return redirect()->route('home');
    }

